// number example
var jsVersion = 5;
console.log('value : ' + jsVersion + ' Type : ' + typeof jsVersion);

// String Examples
var studentName = 'Arjun Reddy';
console.log('value : ' + studentName + ' Type : ' + typeof studentName);

// boolean Examples
var isJSEasy = true;
console.log('value : ' + isJSEasy + ' Type : ' + typeof isJSEasy);

// reassignment example of variables
var abc;
console.log('value : ' + abc + ' Type : ' + typeof abc);

abc = 100;
console.log('value : ' + abc + ' Type : ' + typeof abc);

abc = 'test';
console.log('value : ' + abc + ' Type : ' + typeof abc);

abc = true;
console.log('value : ' + abc + ' Type : ' + typeof abc);

abc = null;
console.log('value : ' + abc + ' Type : ' + typeof abc);